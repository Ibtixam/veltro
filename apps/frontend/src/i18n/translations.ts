// Detect locale from Accept-Language or navigator.language
export function detectLocale(input?: string): Locale {
  if (!input) return 'fr';
  const lang = input.toLowerCase().split(/[,;-]/)[0].trim();
  const supported = SUPPORTED_LOCALES as readonly string[];
  if (supported.includes(lang)) return lang as Locale;
  // Map language variants
  const map: Record<string,Locale> = {'zh-cn':'zh','zh-tw':'zh','pt-br':'pt','pt-pt':'pt'};
  return map[lang] ?? 'fr';
}

export function t(locale: Locale, key: string, vars?: Record<string,string|number>): string {
  let val = T[locale]?.[key] ?? T.fr?.[key] ?? T.en?.[key] ?? key;
  if (vars) { for (const [k,v] of Object.entries(vars)) { val = val.replace(new RegExp(`\\{\\{${k}\\}}`, 'g'), String(v)); } }
  return val;
}